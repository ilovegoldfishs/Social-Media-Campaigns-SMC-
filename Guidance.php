                <div class="legislation-item">
                    <h3>General Data Protection Regulation (GDPR)</h3>
                    <p>The GDPR is a comprehensive data protection law that applies to all EU member states and any organization handling EU citizens' data. It sets strict guidelines for data collection, processing, and storage, with significant implications for social media use and data sharing.</p>
                </div>
                <div class="legislation-item">
                    <h3>Children's Online Privacy Protection Act (COPPA)</h3>
                    <p>COPPA is a United States federal law that imposes certain requirements on operators of websites or online services directed to children under 13 years of age, and on operators of other websites or online services that have actual knowledge that they are collecting personal information online from a child under 13 years of age.</p>
                </div>
                <div class="legislation-item">
                    <h3>Digital Millennium Copyright Act (DMCA)</h3>
                    <p>The DMCA is a United States copyright law that addresses copyright issues in the digital age. It has significant implications for content sharing on social media platforms, including provisions for takedown notices and safe harbor protections for service providers.</p>
                </div>
            </section>

